/* ─── CJK font override ─── */
export function getDisplayFont(lang: Lang): string {
  if (lang === "ko") return "'Noto Serif KR', var(--font-display)";
  if (lang === "ja") return "'Noto Serif JP', var(--font-display)";
  if (lang === "zh") return "'Noto Serif SC', var(--font-display)";
  return "var(--font-display)";
}

export function getBodyFont(lang: Lang): string {
  if (lang === "ko") return "'Noto Sans KR', var(--font-body)";
  if (lang === "ja") return "'Noto Sans JP', var(--font-body)";
  if (lang === "zh") return "'Noto Sans SC', var(--font-body)";
  return "var(--font-body)";
}

/* Hero font size adjustment for CJK */
export function getHeroSize(lang: Lang): string {
  if (lang === "ko" || lang === "ja" || lang === "zh") return "clamp(40px, 6vw, 80px)";
  return "clamp(48px, 7vw, 96px)";
}

/* ─── Context ─── */
interface LangContextType {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: (key: string) => string;
  displayFont: string;
  bodyFont: string;
}

const LangContext = createContext<LangContextType | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    if (typeof window !== "undefined") {
      return (localStorage.getItem("tc-lang") as Lang) || "en";
    }
    return "en";
  });

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    localStorage.setItem("tc-lang", l);
  }, []);

  const t = useCallback(
    (key: string): string => translations[lang]?.[key] ?? translations.en[key] ?? key,
    [lang]
  );

  return (
    <LangContext.Provider value={{ lang, setLang, t, displayFont: getDisplayFont(lang), bodyFont: getBodyFont(lang) }}>
      {children}
    </LangContext.Provider>
  );
}

export function useLang() {
  const ctx = useContext(LangContext);
  if (!ctx) throw new Error("useLang must be within LanguageProvider");
  return ctx;
}
